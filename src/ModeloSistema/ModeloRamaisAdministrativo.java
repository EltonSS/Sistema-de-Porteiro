package ModeloSistema;

public class ModeloRamaisAdministrativo {

    private int codRm;
    private String NumRm;
    private String descSet;

    public int getCodRm() {
        return codRm;
    }

    public void setCodRm(int codRm) {
        this.codRm = codRm;
    }

    public String getNumRm() {
        return NumRm;
    }

    public void setNumRm(String NumRm) {
        this.NumRm = NumRm;
    }

    public String getDescSet() {
        return descSet;
    }

    public void setDescSet(String descSet) {
        this.descSet = descSet;
    }
}
