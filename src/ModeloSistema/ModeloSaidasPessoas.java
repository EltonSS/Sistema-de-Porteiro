package ModeloSistema;

public class ModeloSaidasPessoas {

    private int codEnt;
    private String dataSaid;
    private String HoraSaid;
    private String nomePs;
    private String detalhesPs;
    private String nomePort;
    private String obsSaid;

    public int getCodEnt() {
        return codEnt;
    }

    public void setCodEnt(int codEnt) {
        this.codEnt = codEnt;
    }

    public String getDataSaid() {
        return dataSaid;
    }

    public void setDataSaid(String dataSaid) {
        this.dataSaid = dataSaid;
    }

    public String getHoraSaid() {
        return HoraSaid;
    }

    public void setHoraSaid(String HoraSaid) {
        this.HoraSaid = HoraSaid;
    }

    public String getNomePs() {
        return nomePs;
    }

    public void setNomePs(String nomePs) {
        this.nomePs = nomePs;
    }

    public String getNomePort() {
        return nomePort;
    }

    public void setNomePort(String nomePort) {
        this.nomePort = nomePort;
    }

    public String getObsSaid() {
        return obsSaid;
    }

    public void setObsSaid(String obsSaid) {
        this.obsSaid = obsSaid;
    }

    public String getDetalhesPs() {
        return detalhesPs;
    }

    public void setDetalhesPs(String detalhesPs) {
        this.detalhesPs = detalhesPs;
    }
}
