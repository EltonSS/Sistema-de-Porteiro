package ModeloSistema;

public class ModeloVisitantes {

    private int codVst;
    private String nomeVst;
    private String Obs;

    public int getCodVst() {
        return codVst;
    }

    public void setCodVst(int codVst) {
        this.codVst = codVst;
    }

    public String getNomeVst() {
        return nomeVst;
    }

    public void setNomeVst(String nomeVst) {
        this.nomeVst = nomeVst;
    }

    public String getObs() {
        return Obs;
    }

    public void setObs(String Obs) {
        this.Obs = Obs;
    }
}
