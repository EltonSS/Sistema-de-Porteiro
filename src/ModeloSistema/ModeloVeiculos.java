package ModeloSistema;

public class ModeloVeiculos {

    private int codVc;
    private String nomePs;
    private String descMc;
    private String descMd;
    private String descCor;
    private String detalhesProp;
    private String placaVc;

    public int getCodVc() {
        return codVc;
    }

    public void setCodVc(int codVc) {
        this.codVc = codVc;
    }
    
    public String getNomePs() {
        return nomePs;
    }

    public void setNomePs(String nomePs) {
        this.nomePs = nomePs;
    }

    public String getDescMc() {
        return descMc;
    }

    public void setDescMc(String descMc) {
        this.descMc = descMc;
    }

    public String getDescMd() {
        return descMd;
    }

    public void setDescMd(String descMd) {
        this.descMd = descMd;
    }
 
    public String getDescCor() {
        return descCor;
    }

    public void setDescCor(String descCor) {
        this.descCor = descCor;
    }

    public String getDetalhesProp() {
        return detalhesProp;
    }

    public void setDetalhesProp(String detalhesProp) {
        this.detalhesProp = detalhesProp;
    }

    public String getPlacaVc() {
        return placaVc;
    }

    public void setPlacaVc(String placaVc) {
        this.placaVc = placaVc;
    }
}