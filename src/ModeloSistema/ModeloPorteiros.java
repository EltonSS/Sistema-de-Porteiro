package ModeloSistema;

public class ModeloPorteiros {

    private int codPort;
    private String nomePort;  
    private String loginPort;
    private String senhaPort;

    public int getCodPort() {
        return codPort;
    }

    public void setCodPort(int codPort) {
        this.codPort = codPort;
    }

    public String getNomePort() {
        return nomePort;
    }

    public void setNomePort(String nomePort) {
        this.nomePort = nomePort;
    }

    public String getLoginPort() {
        return loginPort;
    }

    public void setLoginPort(String loginPort) {
        this.loginPort = loginPort;
    }

    public String getSenhaPort() {
        return senhaPort;
    }

    public void setSenhaPort(String senhaPort) {
        this.senhaPort = senhaPort;
    }
}
