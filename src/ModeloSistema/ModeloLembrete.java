
package ModeloSistema;

public class ModeloLembrete {
       
    private String dataLembrete;
    private String assuntoLembrete;
    private String observacaoLembrete;

   
    public String getDataLembrete() {
        return dataLembrete;
    }

    public void setDataLembrete(String dataLembrete) {
        this.dataLembrete = dataLembrete;
    }
    
    public String getAssuntoLembrete() {
        return assuntoLembrete;
    }

    public void setAssuntoLembrete(String assuntoLembrete) {
        this.assuntoLembrete = assuntoLembrete;
    }

    public String getObservacaoLembrete() {
        return observacaoLembrete;
    }

    public void setObservacaoLembrete(String observacaoLembrete) {
        this.observacaoLembrete = observacaoLembrete;
    }
   
}