package ModeloSistema;

public class ModeloApartamento {
    
    private int codApt;    
    private String numApt;
    private String ramalApt;
    private String descTrr;   

    public int getCodApt() {
        return codApt;
    }

    public void setCodApt(int codApt) {
        this.codApt = codApt;
    }

    public String getNumApt() {
        return numApt;
    }

    public void setNumApt(String numApt) {
        this.numApt = numApt;
    }

    public String getRamalApt() {
        return ramalApt;
    }

    public void setRamalApt(String ramalApt) {
        this.ramalApt = ramalApt;
    }

    public String getDescTrr() {
        return descTrr;
    }

    public void setDescTrr(String descTrr) {
        this.descTrr = descTrr;
    }
   
}