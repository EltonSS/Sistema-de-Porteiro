package ModeloSistema;

public class ModeloChaves {

    private int codChv;
    private String descChv;
    private String descSet;

    public int getCodChv() {
        return codChv;
    }

    public void setCodChv(int codChv) {
        this.codChv = codChv;
    }

    public String getDescChv() {
        return descChv;
    }

    public void setDescChv(String descChv) {
        this.descChv = descChv;
    }

    public String getDescSet() {
        return descSet;
    }

    public void setDescSet(String descSet) {
        this.descSet = descSet;
    }
}
