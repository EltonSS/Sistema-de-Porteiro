package ModeloSistema;

public class ModeloMoradores {

    private int codMrd;
    private String nomeMrd;
    private String torreApt;
    private String numApt;

    public int getCodMrd() {
        return codMrd;
    }

    public void setCodMrd(int codMrd) {
        this.codMrd = codMrd;
    }

    public String getNomeMrd() {
        return nomeMrd;
    }

    public void setNomeMrd(String nomeMrd) {
        this.nomeMrd = nomeMrd;
    }

    public String getTorreApt() {
        return torreApt;
    }

    public void setTorreApt(String torreApt) {
        this.torreApt = torreApt;
    }

    public String getNumApt() {
        return numApt;
    }

    public void setNumApt(String numApt) {
        this.numApt = numApt;
    }
}
