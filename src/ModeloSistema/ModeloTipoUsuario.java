
package ModeloSistema;


public class ModeloTipoUsuario {
    
    private int codTipoUsuario;
    private String DescricaoTipoUsuario;
    private String pesquisaTipoUsuario;

    public int getCodTipoUsuario() {
        return codTipoUsuario;
    }
    
    public void setCodTipoUsuario(int codTipoUsuario) {
        this.codTipoUsuario = codTipoUsuario;
    }

    public String getDescricaoTipoUsuario() {
        return DescricaoTipoUsuario;
    }

    public void setDescricaoTipoUsuario(String DescricaoUsuario) {
        this.DescricaoTipoUsuario = DescricaoUsuario;
    }

    public String getPesquisaTipoUsuario() {
        return pesquisaTipoUsuario;
    }

    public void setPesquisaTipoUsuario(String pesquisaTipoUsuario) {
        this.pesquisaTipoUsuario = pesquisaTipoUsuario;
    }
      
     
    
}