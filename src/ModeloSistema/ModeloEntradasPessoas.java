package ModeloSistema;

public class ModeloEntradasPessoas {

    private int codEnt;
    private String dataEnt;
    private String horaEnt;
    private String rgPs;
    private String detalhesPs;
    private String nomePort;
    private String obsEnt;
    private int entrou;

    public int getCodEnt() {
        return codEnt;
    }

    public void setCodEnt(int codEnt) {
        this.codEnt = codEnt;
    }

    public String getDataEnt() {
        return dataEnt;
    }

    public void setDataEnt(String dataEnt) {
        this.dataEnt = dataEnt;
    }

    public String getHoraEnt() {
        return horaEnt;
    }

    public void setHoraEnt(String horaEnt) {
        this.horaEnt = horaEnt;
    }

    public String getRgPs() {
        return rgPs;
    }

    public void setRgPs(String rgPs) {
        this.rgPs = rgPs;
    }

    public String getNomePort() {
        return nomePort;
    }

    public void setNomePort(String nomePort) {
        this.nomePort = nomePort;
    }

    public String getObsEnt() {
        return obsEnt;
    }

    public void setObsEnt(String obsEnt) {
        this.obsEnt = obsEnt;
    }

    public int getEntrou() {
        return entrou;
    }

    public void setEntrou(int entrou) {
        this.entrou = entrou;
    }

    public String getDetalhesPs() {
        return detalhesPs;
    }

    public void setDetalhesPs(String detalhesPs) {
        this.detalhesPs = detalhesPs;
    }
}
