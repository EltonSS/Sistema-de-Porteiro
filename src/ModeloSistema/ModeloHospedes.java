
package ModeloSistema;

public class ModeloHospedes {
    private int codHosp; 
    private String nomeHosp;
    private String torreApt;
    private String numApt;

    public int getCodHosp() {
        return codHosp;
    }

    public void setCodHosp(int codHosp) {
        this.codHosp = codHosp;
    }

    public String getNomeHosp() {
        return nomeHosp;
    }

    public void setNomeHosp(String nomeHosp) {
        this.nomeHosp = nomeHosp;
    }

    public String getTorreApt() {
        return torreApt;
    }

    public void setTorreApt(String torreApt) {
        this.torreApt = torreApt;
    }

    public String getNumApt() {
        return numApt;
    }

    public void setNumApt(String numApt) {
        this.numApt = numApt;
    }
    
}